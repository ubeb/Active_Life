class ProfileData {
  final String name;
  final String age;
  final String weight;
  final String height;
  final String email;
  final int phone;
  final String password;
  final String gender;

  ProfileData({
    required this.name,
    required this.age,
    required this.weight,
    required this.height,
    required this.email,
    required this.phone,
    required this.password,
    required this.gender,
  });
}
